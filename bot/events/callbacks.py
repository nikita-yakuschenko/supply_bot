from bot.commands.user import save_user_to_json
import logging

# –£–¥–∞–ª—è–µ–º –∏–º–ø–æ—Ä—Ç –∏–∑ main, —á—Ç–æ–±—ã –∏–∑–±–µ–∂–∞—Ç—å —Ü–∏–∫–ª–∏—á–µ—Å–∫–æ–≥–æ –∏–º–ø–æ—Ä—Ç–∞
